#1 - Faça um progarama que receba quatro números inteiros, calcule e mostre a soma desses números.
num1 = int(input('Escreva o primeiro número: '))
num2 = int(input('Escreva o segundo número: '))
num3 = int(input('Escreva o terceiro número: '))
num4 = int(input('Escreva o quarto número: '))
soma = num1+num2+num3+num4
print('Seu resultado é: ',soma)